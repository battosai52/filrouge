				En 2009, Infogrames Entertainment se renomme Atari SA, mais dépose le bilan en 2013, entrainant dans sa chute Atari Inc. et Atari Interactive Inc. (et toutes ses filiales). Atari Inc. et Atari SA ont simplement exploité les licences Atari sur diverses plates-formes.

